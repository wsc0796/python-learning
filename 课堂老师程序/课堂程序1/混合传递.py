# 如果函数的形参 进行混合参数传递必须遵循以下规则
# 1.带有默认值的参数必须位于普通参数之后
# 2.带有“*“标识的参数必须位于带有默认值参数之后
# 3.带有“**“标识的参数必须位于带有“*”标识参数之后
def test(a,b,c=33,*args,**ksargs):# 定义函数
    print(a,b,c,args,ksargs)
test(1,2)# 1 2 33 () {}
test(1,2,3)# 1 2 3 () {}
test(1,2,3,4)#1 2 3 (4,) {}
test(1,2,3,4,e=5)# 1 2 3 (4,) {'e': 5}
test(1,2,3,4,5,7,8,e=5,f=7)# 1 2 3 (4,5,7,8) {"e":5,"f":7}
