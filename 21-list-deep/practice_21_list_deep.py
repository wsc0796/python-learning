# ============================================================
# 练习1：增删改查
# ============================================================
lst = [10, 20, 30]

# TODO: 末尾加40 → [10, 20, 30, 40]
lst.append(40)  # 把 None 改成 40
print(lst)

# TODO: 在索引1位置插入15 → [10, 15, 20, 30, 40]
lst.insert(1, 15)  # 填索引和值
print(lst)

# TODO: 删除30
lst.remove(30)
print(lst)

# TODO: 弹出最后一个元素
last=lst.pop()
print(last,lst)


# ============================================================
# 练习2：排序
# ============================================================
scores = [78, 92, 85, 88, 60, 95]

# TODO: 降序排序
scores.sort(reverse= True)  # 填参数
print(scores)      # [95, 92, 88, 85, 78, 60]

# TODO: 用 sorted 按分数排序学生，降序
students = [("张三", 78), ("李四", 92), ("王五", 85)]
sorted_students = sorted(students, key=lambda x:x[1], reverse=True)  # 填 key 和 reverse
print(sorted_students)  # [('李四', 92), ('王五', 85), ('张三', 78)]


# ============================================================
# 练习3：嵌套列表
# ============================================================
grid = [
    [1, 2, 3],
    [4, 5, 6],
    [7, 8, 9],
]
# TODO: 提取所有偶数的和
total = 0
for row in grid:
    for val in row:
        if val % 2 == 0:  # 填条件
            total += val
print(f"偶数和: {total}")   # 期望 2+4+6+8 = 20


