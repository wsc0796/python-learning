# 'hello woRld'进行大小写转换
old_string='hello woRld'
upper_str=old_string.upper()# 字符串字母转换成大写字母
print(upper_str)# HELLO WORLD
lower_str=old_string.lower()# 字符串字母转换成小写字母
print(lower_str)# hello world
cap_str=old_string.capitalize()# 字符串首字母转换成大写字母
print(cap_str)# Hello world
titie_str=old_string.title()# 字符串每个单词首字母转换成大写字母
print(titie_str)# Hello World