def group_anagrams(strs):
    """这就是第一题的全部代码，只有6行"""
    groups = {}
    for word in strs:
        key = "".join(sorted(word))     # 第1+2步：排序+拼回
        if key not in groups:           # key不存在？先建一个空列表
            groups[key] = []
        groups[key].append(word)        # 把单词放进去
    return list(groups.values())        # 只取"组"，不要key

